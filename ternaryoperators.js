//ternary operators.

// to avoid code being toooo long, we use these

// AND operator

let age = 20
let name = age > 19 && "ahmad"; 
// so if age is greater than 19, then name will
// be set to ahmad.


// OR operator

let name2 = age > 19 || "ahmad"; 

// if else operator
let name3 = age > 19 ? "ahmad" : "jack"; 
// if age > 19 then set it to ahmad, else set it to jack.

